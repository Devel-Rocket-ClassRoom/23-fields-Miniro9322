using System;

class ScoreTracker
{
    private const int MaxScore = 100;
    private const int MinScore = 0;
    private readonly string _subject;
    private int _currentScore = 0;
    private int _bonusCount = 0;

    public ScoreTracker(string subject)
    {
        this._subject = subject;
    }

    public void SetScore(int score)
    {
        if(score < MinScore || score > MaxScore)
        {
            Console.WriteLine("점수는 0~100 사이여야 합니다.");
        }
        else
        {
            _currentScore = score;
            Console.WriteLine($"점수를 {score}점으로 설정했습니다.");
        }
        
    }

    public void AddBonus(int bonus)
    {
        _currentScore += bonus;
        Console.Write($"{bonus}점 보너스 적용! ");
        _bonusCount++;
        if(_currentScore > 100)
        {
            _currentScore = MaxScore;
            Console.WriteLine($"현재 점수: {_currentScore} (최대 점수 도달)");
        }
        else
        {
            Console.WriteLine($"현재 점수: {_currentScore}");
        }
    }

    public void ShowScore()
    {
        Console.WriteLine($"=== {_subject} ===");
        Console.WriteLine($"점수: {_currentScore} / {MaxScore}");
        Console.WriteLine($"보너스 적용 횟수: {_bonusCount}");
    }
}